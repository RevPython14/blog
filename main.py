@app.route('/login', methods=["GET", "POST"])
def login():
    form = LoginForm()
    if form.validate_on_submit():
        user = db.session.execute(db.select(User).filter_by(email=request.form.get("email"))).scalar()
        account_exists = user is not None
        pw_matches = check_password_hash(user.password, request.form.get("password"))
        if pw_matches:
            login_user(user)
            return redirect("/")
        else:
            flash("Invalid username or password.")
            return render_template("login.html", form=form)
    return render_template("login.html", form=form)


