@app.route('/register', methods=["GET", "POST"])
def register():
    form = RegisterForm()
    if form.validate_on_submit():
        hashed_pw = generate_password_hash(
            request.form.get("password"),
            method="pbkdf2:sha256",
            salt_length=8
        )
        new_user = User(
            name=request.form.get("name"),
            email=request.form.get("email"),
            password=hashed_pw
        )
        email_check = db.session.execute(db.select(User).filter_by(email=new_user.email)).scalar()
        email_exists = email_check is not None
        if email_exists:
            flash("An account using this email address already exists: please log in.")
            return redirect("/login")
        else:
            db.session.add(new_user)
            db.session.commit()
            login_user(new_user)
            return redirect("/")

    return render_template("register.html", form=form)


