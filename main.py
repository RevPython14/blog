def admin_only(func):
    @wraps(func)
    def inner(*args, **kwargs):
        if current_user.is_authenticated:
            if current_user.id == 1:
                return func(*args, **kwargs)
            else:
                abort(403)
        else:
            abort(403)
    return inner


